// Enhanced SEO utility functions
export const generatePageTitle = (pageTitle?: string, lang: string = 'en'): string => {
  const langConfig = SEO_CONFIG.languages[lang as keyof typeof SEO_CONFIG.languages];
  if (!pageTitle) return langConfig?.title || SEO_CONFIG.defaultTitle;
  return `${pageTitle} | ${SEO_CONFIG.siteName}`;
};

export const generatePageDescription = (pageDescription?: string, lang: string = 'en'): string => {
  const langConfig = SEO_CONFIG.languages[lang as keyof typeof SEO_CONFIG.languages];
  return pageDescription || langConfig?.description || SEO_CONFIG.defaultDescription;
};

export const generatePageKeywords = (additionalKeywords: string[] = [], lang: string = 'en'): string => {
  const langConfig = SEO_CONFIG.languages[lang as keyof typeof SEO_CONFIG.languages];
  const baseKeywords = langConfig?.keywords || '';
  const allKeywords = [baseKeywords, ...additionalKeywords].filter(Boolean);
  return allKeywords.join(', ');
};

export const generateCanonicalUrl = (path: string = ''): string => {
  return `${SEO_CONFIG.siteUrl}${path}`;
};

export const generateAlternateUrls = (path: string = '') => {
  return {
    en: `${SEO_CONFIG.siteUrl}${path}`,
    ar: `${SEO_CONFIG.siteUrl}/ar${path}`,
    it: `${SEO_CONFIG.siteUrl}/it${path}`,
    fr: `${SEO_CONFIG.siteUrl}/fr${path}`
  };
};

export const generateHreflangTags = (path: string = '') => {
  const alternates = generateAlternateUrls(path);
  return Object.entries(alternates).map(([lang, url]) => ({
    hreflang: lang,
    href: url
  }));
};